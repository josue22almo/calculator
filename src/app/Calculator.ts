
export class Calculator {
  public add(a: number, b: number): number {
    return a + b;
  }

  public substract(a: number, b: number): number {
    return a - b;
  }

  public product(a: number, b: number): number {
    return a * b;
  }

  public divide(a: number, b: number): number {
    return a / b;
  }

}